# Bucles. Repetir una o varias veces unas líneas de código
# Declaración del bucle y cuerpo del bucle
# Tipos
# Determinado: Se ejecutan un número indeterminado de veces. Se sabe a priori cuantas veces se va a ejecutar el código
# en el interior del bucle
# indeterminado: Se ejecutan un número indeterminado de veces. No se sabe a priori cúantas veces se va a ejecutar el
# código en el interior del bucle.
# El número de veces que se ejecutará dependerá de las circunstancias durante la ejecución del programa.
# Bucle FOR: for variable in elemento a recorrer:
# identacion Cuerpo del bucle

# Ejemplo:
estaciones = ['primavera', 'verano', 'otoño', 'invierno']
print('4 estaciones del año')
for i in estaciones:
    print(i)

